// Search function for chatbot
export const searchWebsiteData = (query: string): string => {
  const lowercaseQuery = query.toLowerCase();
  
  // Check for workshop-related queries
  if (lowercaseQuery.includes('workshop') || lowercaseQuery.includes('training')) {
    const relevantWorkshops = websiteData.workshops.filter(workshop => 
      workshop.name.toLowerCase().includes(lowercaseQuery) ||
      workshop.description.toLowerCase().includes(lowercaseQuery) ||
      lowercaseQuery.includes(workshop.name.toLowerCase())
    );
    
    if (relevantWorkshops.length > 0) {
      return relevantWorkshops.map(workshop => 
        `${workshop.name}: ${workshop.description} Duration: ${workshop.duration}, Price: ${workshop.price}`
      ).join('\n\n');
    }
    
    return `We offer ${websiteData.workshops.length} specialized workshops including ${websiteData.workshops.map(w => w.name).join(', ')}. Each workshop is designed to provide practical, actionable strategies for business growth.`;
  }
  
  // Check for service-related queries
  if (lowercaseQuery.includes('service') || lowercaseQuery.includes('consulting')) {
    const relevantServices = websiteData.services.filter(service =>
      service.name.toLowerCase().includes(lowercaseQuery) ||
      service.description.toLowerCase().includes(lowercaseQuery)
    );
    
    if (relevantServices.length > 0) {
      return relevantServices.map(service =>
        `${service.name}: ${service.description}`
      ).join('\n\n');
    }
    
    return `Our services include ${websiteData.services.map(s => s.name).join(', ')}. We provide comprehensive consulting and support across all aspects of business growth.`;
  }
  
  // Check for domain expertise queries
  if (lowercaseQuery.includes('domain') || lowercaseQuery.includes('industry') || lowercaseQuery.includes('sector')) {
    return `We specialize in ${websiteData.domains.map(d => d.name).join(', ')}. Each domain has specific expertise and tailored solutions for industry challenges.`;
  }
  
  // Check for growth strategy queries
  if (lowercaseQuery.includes('growth') || lowercaseQuery.includes('scale') || lowercaseQuery.includes('expand')) {
    return `Our growth strategies focus on scaling without equity dilution through: ${websiteData.growthStrategies.map(s => s.name).join(', ')}. We've helped 500+ startups achieve smart growth with a 93% success rate.`;
  }
  
  // Check for pricing queries
  if (lowercaseQuery.includes('price') || lowercaseQuery.includes('cost') || lowercaseQuery.includes('fee')) {
    return `Workshop pricing varies by program: ${websiteData.workshops.map(w => `${w.name}: ${w.price}`).join(', ')}. We also offer group rates and early bird discounts. Contact us for detailed pricing information.`;
  }
  
  // Check for application/incubation queries
  if (lowercaseQuery.includes('apply') || lowercaseQuery.includes('incubation') || lowercaseQuery.includes('join')) {
    return `To apply for our incubation program or join our workshops, please fill out our contact form or email us at ${websiteData.contact.email}. We'll schedule a consultation call to discuss your needs and provide personalized guidance.`;
  }
  
  // Check for success rate queries
  if (lowercaseQuery.includes('success') || lowercaseQuery.includes('rate') || lowercaseQuery.includes('track record')) {
    return `We have a 93% success rate with our programs. We've helped 500+ startups achieve smart growth using our zero-dilution strategies and proven methodologies.`;
  }
  
  // Check for contact queries
  if (lowercaseQuery.includes('contact') || lowercaseQuery.includes('reach') || lowercaseQuery.includes('email')) {
    return `You can reach us at ${websiteData.contact.email} or call us at ${websiteData.contact.phone}. We're located in ${websiteData.contact.address}.`;
  }
  
  // Check FAQs
  const relevantFaq = websiteData.faqs.find(faq =>
    faq.question.toLowerCase().includes(lowercaseQuery) ||
    lowercaseQuery.includes(faq.question.toLowerCase())
  );
  
  if (relevantFaq) {
    return `${relevantFaq.question}\n\n${relevantFaq.answer}`;
  }
  
  // Default response with general information
  return `${websiteData.about.mission} We offer specialized workshops, consulting services across multiple domains, and proven growth strategies that help startups scale without equity dilution. Our 93% success rate speaks to the effectiveness of our methodologies. How can I help you specifically with your business growth journey?`;
};